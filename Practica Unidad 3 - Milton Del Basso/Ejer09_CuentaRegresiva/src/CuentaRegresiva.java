/*Cuenta regresiva desde el 100 al 0*/ 

public class CuentaRegresiva {
    public static void main(String[] args) throws Exception {
        //variables
        int iInicial=100;
        
        System.out.println("\n");
        System.out.println("Cuenta regresiva desde el 100 al 0: ");
        for(int i=0;i<=100;i++){
            System.out.println(iInicial-i);
        }
    }
}
